// Ordinary JS
function displayImg(){
    const ordinaryImg = document.getElementById("ordinary-img");
    ordinaryImg.style.display = "block";
    const ordinaryVideo = document.getElementById("ordinary-video");
    ordinaryVideo.style.display = "none";
}

function displayVideo(){
    const ordinaryImg = document.getElementById("ordinary-img");
    ordinaryImg.style.display = "none";
    const ordinaryVideo = document.getElementById("ordinary-video");
    ordinaryVideo.style.display = "block";
}

